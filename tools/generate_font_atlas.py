# Layout constants
GLYPH_W = 5       # drawn glyph width in pixels
GLYPH_H = 7       # drawn glyph height in pixels
CELL_W  = 6       # cell width (glyph + 1px right padding)
CELL_H  = 9       # cell height (glyph + 2px bottom padding)
COLUMNS = 16      # glyphs per row in the atlas
SCALE   = 1       # pixel scale (1 = native, 2 = double-size)

def main():
    script_dir = os.path.dirname(os.path.abspath(__file__))
    project_root = os.path.dirname(script_dir)
    output_dir = os.path.join(project_root, "assets", "fonts")
    os.makedirs(output_dir, exist_ok=True)

    chars = sorted(FONT_5X7.keys())
    rows = (len(chars) + COLUMNS - 1) // COLUMNS

    atlas_w = COLUMNS * CELL_W * SCALE
    atlas_h = rows * CELL_H * SCALE

    # RGBA image, fully transparent
    image = Image.new("RGBA", (atlas_w, atlas_h), (0, 0, 0, 0))
    pixels = image.load()

    glyphs = {}

    for idx, code in enumerate(chars):
        col = idx % COLUMNS
        row = idx // COLUMNS
        bitmap = FONT_5X7[code]

        cell_x = col * CELL_W * SCALE
        cell_y = row * CELL_H * SCALE

        for gy in range(GLYPH_H):
            row_bits = bitmap[gy]
            for gx in range(GLYPH_W):
                if row_bits & (0x10 >> gx):
                    for sy in range(SCALE):
                        for sx in range(SCALE):
                            px = cell_x + gx * SCALE + sx
                            py = cell_y + gy * SCALE + sy
                            pixels[px, py] = (255, 255, 255, 255)

        glyphs[chr(code)] = {
            "x": cell_x,
            "y": cell_y,
            "w": GLYPH_W * SCALE,
            "h": GLYPH_H * SCALE,
            "cellW": CELL_W * SCALE,
            "cellH": CELL_H * SCALE,
        }

    png_path = os.path.join(output_dir, "pixel_font.png")
    json_path = os.path.join(output_dir, "pixel_font.json")

    image.save(png_path, "PNG")

    metrics = {
        "atlasWidth": atlas_w,
        "atlasHeight": atlas_h,
        "glyphWidth": GLYPH_W * SCALE,
        "glyphHeight": GLYPH_H * SCALE,
        "cellWidth": CELL_W * SCALE,
        "cellHeight": CELL_H * SCALE,
        "glyphs": glyphs,
    }

    with open(json_path, "w") as f:
        json.dump(metrics, f, indent=2)

    print(f"Font atlas: {png_path} ({atlas_w}x{atlas_h})")
    print(f"Metrics:    {json_path} ({len(glyphs)} glyphs)")


if __name__ == "__main__":
    main()
